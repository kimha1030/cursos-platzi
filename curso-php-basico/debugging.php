<?php

$arr_personas = [
    "Sofia" => 22,
    "Tom" => 56,
    "Leo" => 10
];

/*
var_dump nos permite inspeccionar la variable, ademas muestra tipo de dato, 
    elementos (su respectivo tipo de dato) y contenido de la variable.
*/
var_dump($arr_personas);

/* 
print_r solo me muestra el contenido de una manera mas ordenada. 
Se usa para debuguear que hay en una variable 
*/
print_r($arr_personas);
